int Recursive_count(SinglyLinkedListNode *p)
{
    int count = 0;
    if (p == 0) // base case
    {
        return 0;
    }
    else
        return Recursive_count(p->next) + 1; // recursive call.
}

SinglyLinkedListNode *insertNodeAtPosition(SinglyLinkedListNode *llist, int data, int index)
{

    SinglyLinkedListNode *t;
    SinglyLinkedListNode *first;
    first = llist;

    if (index < 0 || index > Recursive_count(llist)) // invalid insertion
        return 0;

    t = new SinglyLinkedListNode(data);

    if (index == 0)
    {
        t->next = first;
        first = t;
    }
    else
    {
        for (int i = 0; i < index - 1; i++)
        {
            llist = llist->next;
        }
        t->next = llist->next;
        llist->next = t;
    }
    return first;
}

